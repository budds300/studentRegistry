    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int a = JOptionPane.showConfirmDialog(null, "Do you really want to update your informaion?", "Select", JOptionPane.YES_NO_OPTION);
        if (a == 0) {
           

        
            String name = jTextField1.getText();
            String SurName = jTextField2.getText();
            String mobile = jTextField3.getText();
            String faculty = (String) jComboBox2.getSelectedItem();
            String email = jTextField6.getText();
            String gender = (String) jComboBox1.getSelectedItem();
            String address = jTextField10.getText();
            String dateOfBirth = jTextField4.getText();
            String admissionDate = jTextField5.getText();
            if (jComboBox1.getSelectedItem().toString().equals("select your gender")) {
                JOptionPane.showMessageDialog(null, "select a gender");
            } else {
                if (name.isEmpty() || SurName.isEmpty() || mobile.isEmpty() || email.isEmpty() || address.isEmpty() || dateOfBirth.isEmpty() || admissionDate.isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Fill in all fields");
                } else {
                    try {
                        Connection con = ConnectionProvider.getCon();

                        PreparedStatement st = con.prepareStatement("update student set name='" + name + "',surname='" + SurName + "',mobile='" + mobile + "',faculty='" + faculty + "',dateofbirth='" + dateOfBirth + "',admissoinDate='" + admissionDate + "',address='" + address + "',emailAddress='" + email + "',gender='" + gender + "' where id='" + id + "'");
                        st.executeUpdate();
                        JOptionPane.showMessageDialog(null, "Successfully Updated");
                        dispose();
//                        new NewStudent().setVisible(true);
                    } catch (Exception e) {
                        System.out.println(e.getMessage());
                        JOptionPane.showMessageDialog(null, "Something went wrong");

                    }
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

